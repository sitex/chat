def load_token(key: str) -> str:
    env_file = PROJECTS / f"chat-{key}" / ".env"
    for line in env_file.read_text().splitlines():
        line = line.strip()
        if line.startswith("BOT_TOKEN="):
            return line.split("=", 1)[1].strip()
    raise ValueError(f"BOT_TOKEN не найден в {env_file}")


def set_descriptions(token: str, description: str, short: str) -> tuple[bool, bool]:
    base = f"https://api.telegram.org/bot{token}"
    with httpx.Client(timeout=15) as client:
        r1 = client.post(f"{base}/setMyDescription", json={"description": description})
        r2 = client.post(f"{base}/setMyShortDescription", json={"short_description": short})
    return r1.json().get("ok", False), r2.json().get("ok", False)


def main() -> None:
    errors = 0
    for p in PERSONAS:
        key = p["key"]
        try:
            token = load_token(key)
        except Exception as e:
            print(f"❌ {key}: {e}")
            errors += 1
            continue

        desc_len = len(p["description"])
        short_len = len(p["short"])
        if desc_len > 512:
            print(f"⚠️  {key}: description {desc_len} chars > 512, обрезаю")
            p["description"] = p["description"][:512]
        if short_len > 120:
            print(f"⚠️  {key}: short {short_len} chars > 120, обрезаю")
            p["short"] = p["short"][:120]

        ok_desc, ok_short = set_descriptions(token, p["description"], p["short"])
        status = "✅" if ok_desc and ok_short else "⚠️ "
        print(f"{status} {key}: desc={'OK' if ok_desc else 'FAIL'} short={'OK' if ok_short else 'FAIL'} ({desc_len}ch)")
        if not ok_desc or not ok_short:
            errors += 1

    print(f"\n{'✅ Готово' if not errors else f'❌ {errors} ошибок'}")


if __name__ == "__main__":
    main()
